def arab2roman(n):
    M = n // 1000
    D = (n-1000*M) // 500
    C = (n-1000*M-500*D) // 100
    L = (n-1000*M-500*D-100*C) // 50
    X = (n-1000*M-500*D-100*C-50*L) // 10
    V = (n-1000*M-500*D-100*C-50*L-10*X) // 5
    I = (n-1000*M-500*D-100*C-50*L-10*X-5*V)
    print(M,D,C,L,X,V,I)

arab2roman(1999)